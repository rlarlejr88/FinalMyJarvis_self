-- 청구서는 반드시 회원(MEMBER_NO)에 의해 작성되며, 계약(CONTRACT_NO)과 연결됩니다.
-- 고객 정보는 별도의 청구 대상자 테이블(TBL_INVOICE_TARGET)을 통해 관리됩니다.
-- 청구 금액, 결제 방식, 발송 여부, 입금 여부 등의 청구 관련 정보는 이 테이블에 기록됩니다.

